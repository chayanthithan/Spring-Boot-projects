package com.sonchayan.loginAndlogout.model;

public class ForgetPassword {
private String email;

    public ForgetPassword() {
    }

    public ForgetPassword(String email) {
        this.email = email;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
